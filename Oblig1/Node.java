public class Node {
    private int prosessorer;
    private int minne;

    public Node(int prosessorer, int minne) {
    this.prosessorer = prosessorer;
    this.minne = minne;
}

    public int getMinne() {
        return minne;
    }
    public int getProsessorer() {
        return prosessorer;
    }
}
